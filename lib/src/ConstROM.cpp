////////////////////////////////////////////////////////
//  名称:
//      BaseROM_Read_Word
//  功能:
//      从ConstROM用于读取16比特数据，代替MMU功能
//  参数:
//      1.RD0:数据地址(字节地址)
//  返回值:
//      1.RD0:16比特数据
////////////////////////////////////////////////////////
Sub ConstROM_Read_Word
{
    push(RA0);

    push(RD0);
    RD0_ClrByteH8;
    RF_ShiftR2(RD0);
    //RD0 += 0xD0000000;
    //RA0 = RD0;
    RD1 = *(const_rom + RD0.m_data);
    pop(RD0);
    if (RD0_Bit1 == 1) goto L_ConstROM_Read_Word_1;
    RF_GetL16(RD1);
    goto L_ConstROM_Read_Word_End;

L_ConstROM_Read_Word_1:
    RF_GetH16(RD1);

L_ConstROM_Read_Word_End:
    RD0 = RD1;
    RD0_SignExtL16;
    pop(RA0);


    Return(0);

}

